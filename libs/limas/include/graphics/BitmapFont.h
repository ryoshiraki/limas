  std::map<char, gl::VboMesh> mesh_map_;

 public:
  static const size_t NUM_CHARS = 45;
  static const size_t CHAR_WIDTH = 5;
  static const size_t CHAR_HEIGHT = 5;
  static const size_t CHAR_SPACE = 1;

  BitmapFont() {}

  void load() {
    try {
      image_ = ImageIO::loadFromMemory(font_data_.data(), font_data_.size());
    } catch (Exception& e) {
      log::error("BitmapFont") << e.what() << log::end();
    }

    geom::Mesh m = prim::Rectangle(0, 0, CHAR_WIDTH + CHAR_SPACE, CHAR_HEIGHT);
    for (const auto& [key, value] : word_map_) {
      float tex_width = 1.0 / NUM_CHARS;
      float tex_offset = getPosition(key);

      gl::VboMesh mesh = m;
      for (auto& tc : mesh.getTexCoords()) {
        tc *= glm::vec2(tex_width, 1);
        tc += glm::vec2(tex_offset, 0);
      }
      mesh.update();
      mesh_map_[key] = mesh;
    }
  }

  std::vector<gl::VboMesh*> getMeshes(const std::string& _text) {
    std::string text = util::toUpper(_text);

    std::vector<gl::VboMesh*> meshes;
    for (int i = 0; i < text.length(); i++) {
      meshes.push_back(&mesh_map_[text[i]]);
    }

    return meshes;
  }

  bool isValid(const std::string& text) const {
    for (int i = 0; i < text.length(); i++) {
      auto c = text.at(i);
      if (word_map_.find(c) == word_map_.end()) return false;
    }
    return true;
  }

  size_t getSize(const std::string& text) const {
    return (CHAR_WIDTH + CHAR_SPACE) * text.length();
  }
  size_t getCharacterWidth() const { return CHAR_WIDTH; }
  size_t getSpaceWidth() const { return CHAR_SPACE; }

  gl::Texture2D& getTexture() { return image_.getTexture(); }

 protected:
  Image image_;

  float getPosition(char c) {
    auto it = word_map_.find(c);
    if (it != word_map_.end()) return it->second / (float)NUM_CHARS;
    log::warn("BitmapFont") << c << " is not supported" << log::end();
    return 0;
  }
