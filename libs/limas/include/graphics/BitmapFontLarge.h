  static const size_t NUM_COLS = 16;
  static const size_t NUM_ROWS = 16;
  static const size_t CHAR_WIDTH = 8;
  static const size_t CHAR_HEIGHT = 14;

  BitmapFontLarge() {}

  void load() {
    try {
      image_ = ImageIO::loadFromMemory(font_data_.data(), font_data_.size());
    } catch (Exception& e) {
      logger::error("BitmapFont") << e.what() << logger::end();
    }

    geom::Mesh m = prim::Rectangle(0, 0, CHAR_WIDTH, CHAR_HEIGHT);

    static const float TEX_WIDTH = 8.0f / 256.0f;
    static const float TEX_HEIGHT = 14.0f / 256.0f;

    for (int i = 32; i < 128; i++) {
      float tex_x = (float)(i % 16) / 16.0;
      float tex_y = ((int)(i / 16.0)) / 16.0;

      gl::VboMesh mesh = m;

      mesh.getTexCoords()[0] = glm::vec2(tex_x, tex_y);
      mesh.getTexCoords()[1] = glm::vec2(tex_x + TEX_WIDTH, tex_y);
      mesh.getTexCoords()[2] = glm::vec2(tex_x + TEX_WIDTH, tex_y + TEX_HEIGHT);
      mesh.getTexCoords()[3] = glm::vec2(tex_x, tex_y + TEX_HEIGHT);
      mesh.updateTexCoords();
      mesh_map_[i] = mesh;
    }
  }

  std::vector<gl::VboMesh*> getMeshes(const std::string& text) {
    std::vector<gl::VboMesh*> meshes;
    for (int i = 0; i < text.length(); i++) {
      if (text[i] >= 32) {
        meshes.push_back(&mesh_map_[text[i]]);
      } else {
        meshes.push_back(&mesh_map_[' ']);
      }
    }

    return meshes;
  }

  size_t getSize(const std::string& text) const {
    return CHAR_WIDTH * text.length();
  }
  size_t getCharacterWidth() const { return CHAR_WIDTH; }
  size_t getCharacterHeight() const { return CHAR_HEIGHT; }

  gl::Texture2D& getTexture() { return image_.getTexture(); }

